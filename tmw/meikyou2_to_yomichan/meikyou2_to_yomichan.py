import sys
import shutil
import re
import json
from pathlib import Path
from bs4 import BeautifulSoup
from css_parser import parseStyle


def main():
    terms = []
    entries = load_meikyo2_json()
    sequence_number = 0
    for entry in entries:
        sequence_number += 1
        heading_parts = get_heading_parts(entry["heading"])
        reading = get_reading(heading_parts[0])
        word_is_chinese = is_chinese_loanword(entry["heading"])
        if reading == "":
            kanji_forms, reading = get_expression(heading_parts[1], entry["text"])
        elif word_is_chinese:
            kanji_forms = get_kanji_forms(heading_parts[2])
        else:
            kanji_forms = get_kanji_forms(heading_parts[1])
        kanji_forms = add_substitute_kanji(kanji_forms)
        definition = get_definition(entry["text"])
        if len(kanji_forms) == 0:
            inflection_rules = get_inflection_rules(entry["text"], "", reading)
            term = [reading, "", None, inflection_rules, 0, definition, sequence_number, ""]
            terms.append(term)
        for idx, kanji_form in enumerate(kanji_forms):
            inflection_rules = get_inflection_rules(entry["text"], kanji_form, reading)
            term = [kanji_form, reading, None, inflection_rules, -idx, definition, sequence_number, ""]
            terms.append(term)
    create_yomichan_zip(terms)


def load_meikyo2_json():
    file_path = sys.argv[1]
    if not Path(file_path).is_file():
        print(f"Invalid file path: {file_path}")
        sys.exit(1)
    with open(file_path, "r", encoding="utf-8") as f:
        data = json.load(f)
    return data["subbooks"][0]["entries"]


def get_clean_text(text):
    text_gaiji_codes = re.findall("\{\{[^\{]+?\}\}", text)
    for code in text_gaiji_codes:
        text = text.replace(code, gaiji_map[code])
    for tag in markup_tags_to_remove:
        text = text.replace(tag, "")
    text = text.replace("<begin_reference>", "")
    text = re.sub("<end_reference page=[0-9]+ offset=[0-9]+>", "", text)
    text = text.removesuffix("<newline>")
    text = text.replace("<newline>", "\n")
    return text


def get_heading_parts(heading):
    heading = get_clean_text(heading)

    heading = heading.replace("<begin_subscript>", "␞")
    heading = heading.replace("<end_subscript>", "␞")
    heading = re.sub("␞[^␞]*␞", "", heading)

    heading = heading.replace("<begin_superscript>", "␞")
    heading = heading.replace("<end_superscript>", "␞")
    heading = re.sub("␞[^␞]*␞", "", heading)

    # format: "reading【kanji forms】[loanwords]"
    all_parts = re.findall("([^\[【]+)?(?:【(.*)】)?(?:\[(.*)\])?", heading)
    return all_parts[0]  # only the first result contains useful info


def get_reading(reading_part):
    reading = re.sub("[‐・]", "", reading_part)
    return reading.strip()


def is_chinese_loanword(heading):
    pattern = "\[.*<begin_subscript>.*中国.*<end_subscript>.*\]"
    return (re.search(pattern, heading) is not None)


def get_expression(kanji_part, entry_text):
    expression = re.sub("[‐・、（）]", "", kanji_part).strip()
    reading = expression_info[expression][0]
    if reading == "ambiguous":
        text = get_clean_text(entry_text)
        if ambiguous_expressions[expression][0] in text:
            reading = ambiguous_expressions[expression][1]
        else:
            reading = ambiguous_expressions[expression][2]
    return [expression], reading


def get_kanji_forms(kanji_part):
    kanji_forms = []
    kanji_part = re.sub("[《》〈〉]", "", kanji_part)
    delimited_headword_group_list = re.findall("([^（]+)?(?:（(.*)）)?", kanji_part)
    for delimited_headword_group in delimited_headword_group_list:
        for delimited_headwords in delimited_headword_group:
            headwords = delimited_headwords.split("・")
            for headword in headwords:
                headword = headword.strip()
                if headword != '' and headword not in kanji_forms:
                    kanji_forms.append(headword)
    return kanji_forms


def add_substitute_kanji(kanji_forms):
    for obscure_character, usual_character in substitute_kanji.items():
        new_forms = []
        for form in kanji_forms:
            if obscure_character in form:
                new_form = form.replace(obscure_character, usual_character)
                new_forms.append(new_form)
        for new_form in new_forms:
            if new_form not in kanji_forms:
                kanji_forms.append(new_form)
    return kanji_forms


def get_definition(text):
    text = get_clean_text(text)

    # Display ▼ and ▽ symbols as rubies over the corresponding kanji
    text = re.sub("<begin_superscript>([▼▽])<end_superscript>(.)", r"<ruby>\2<rt>\1</rt></ruby>", text)

    # Some subscript texts contain single space characters (\s?) for some reason.
    # Add parentheses to the subscripts.
    text = re.sub("<begin_subscript>\s?", "<span style='font-size: small; vertical-align: sub' data-meikyo='furigana'>(", text)
    text = text.replace("<end_subscript>", ")</span>")

    # Add table markup to 関連語 information.
    # There should only be one of these sections per entry at most.
    text = text.replace("\n関連語\n", "\n<table><tr><th colspan=3>関連語</th></tr>\n")
    text = re.sub("\n([^\n]+)‖([^\n]+)‖([^\n]+)", r"<tr><td>\1</td><td>\2</td><td>\3</td></tr>", text)
    final_row_tag_index = text.rfind("</tr>")
    if final_row_tag_index != -1:
        close_table_index = final_row_tag_index + len("</tr>")
        text = text[:close_table_index] + "</table>" + text[close_table_index:]

    text = "<body>" + text + "</body>"
    soup = BeautifulSoup(text, features="html.parser")  # performance bottleneck
    body_structure = get_markup_structure(soup.body)
    content = body_structure["content"]
    if isinstance(content, str):
        return [content]
    else:
        return [{
            "type": "structured-content",
            "content": content
        }]


def get_markup_structure(soup):
    content = []
    for child in soup.children:
        if child.name is None:
            if child != "":
                content.append(child)
        else:
            content.append(get_markup_structure(child))
    node = {"tag": soup.name}
    attributes = get_attributes(soup.attrs)
    for key, val in attributes.items():
        node[key] = val
    if len(content) == 1:
        node["content"] = content[0]
    else:
        node["content"] = content
    return node


def get_attributes(attrs):
    attributes = {}
    if "colspan" in attrs:
        attributes["colSpan"] = int(attrs["colspan"])
    if "style" in attrs:
        attributes["style"] = get_style(attrs["style"])
    if "lang" in attrs:
        attributes["lang"] = attrs["lang"]
    if "data-meikyo" in attrs:
        attributes["data"] = {"meikyo": attrs["data-meikyo"]}
    return attributes


def get_style(inline_style_string):
    style = {}
    parsedStyle = parseStyle(inline_style_string)
    if parsedStyle.fontSize != "":
        style["fontSize"] = parsedStyle.fontSize
    if parsedStyle.verticalAlign != "":
        style["verticalAlign"] = parsedStyle.verticalAlign
    if parsedStyle.textDecoration != "":
        style["textDecorationLine"] = parsedStyle.textDecoration
    return style


def get_inflection_rules(entry_text, kanji_form, reading):
    if kanji_form in expression_info:
        return expression_info[kanji_form][1]
    text = get_clean_text(entry_text)
    tag_sets = re.findall("〘([^〙]*)〙", text)
    rules = set()
    for tag_set in tag_sets:
        for tag in tag_set.split("・"):
            if tag == "形":
                rules.add("adj-i")
            if "一" in tag:
                rules.add("v1")
            if "二" in tag or "四" in tag or "五" in tag:
                rules.add("v5")
            if "サ変" in tag and (kanji_form.endswith("する") or kanji_form == "為る"):
                rules.add("vs")
            if "サ変" in tag and kanji_form.endswith("ずる"):
                rules.add("vz")
            if kanji_form == "来る" and reading == "くる":
                rules.add("vk")
    return " ".join(list(rules))


def create_yomichan_zip(terms):
    build_directory = "yomichan_dictionary_json_files"
    os.mkdir(build_directory)

    terms_per_file = 5000
    max_i = int(len(terms) / terms_per_file) + 1
    for i in range(max_i):
        with open(f"{build_directory}/term_bank_{i+1}.json", "w", encoding='utf8') as f:
            start = terms_per_file * i
            end = terms_per_file * (i + 1)
            json.dump(terms[start:end], f, indent=4, ensure_ascii=False)

    with open(f"{build_directory}/index.json", 'w', encoding='utf8') as f:
        index = {
            "title": "明鏡国語辞典　第二版",
            "revision": "meikyou2.v1",
            "sequenced": True,
            "format": 3,
            "description": "▼ non-jōyō kanji\n▽ jōyō kanji used with a non-standard reading\n\ncircled numbers only go up to ㊿ in unicode, so after that they become Ⓐ, Ⓑ, Ⓒ, etc.\n\nadjust furigana size and positioning by adding custom CSS\n\n[data-sc-meikyo=\"furigana\"] {\nfont-size: 70% !important;\nvertical-align: sub !important;\n}",
        }
        json.dump(index, f, indent=4, ensure_ascii=False)

    zip_filename = "[Monolingual] 明鏡国語辞典　第二版"
    if Path(f"{zip_filename}.zip").is_file():
        os.remove(f"{zip_filename}.zip")
    shutil.make_archive(zip_filename, "zip", build_directory)
    shutil.rmtree(build_directory)


